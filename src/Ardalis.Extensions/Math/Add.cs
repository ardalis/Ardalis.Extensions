namespace Ardalis.Extensions.Math;

// Naming: https://en.wikipedia.org/wiki/Template:Arithmetic_operations
public static partial class MathExtensions
{
  public static int Add(this short augend, short addend)
  {
    int sum = augend + addend;
    return sum;
  }

  public static int Add(this short augend, int addend)
  {
    int sum = augend + addend;
    return sum;
  }

  public static long Add(this short augend, long addend)
  {
    long sum = augend + addend;
    return sum;
  }

  public static int Add(this int augend, short addend)
  {
    int sum = augend + addend;
    return sum;
  }

  public static int Add(this int augend, int addend)
  {
    int sum = augend + addend;
    return sum;
  }

  public static long Add(this int augend, long addend)
  {
    long sum = augend + addend;
    return sum;
  }

  public static long Add(this long augend, short addend)
  {
    long sum = augend + addend;
    return sum;
  }

  public static long Add(this long augend, int addend)
  {
    long sum = augend + addend;
    return sum;
  }

  public static long Add(this long augend, long addend)
  {
    long sum = augend + addend;
    return sum;
  }
}
