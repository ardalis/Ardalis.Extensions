namespace Ardalis.Extensions.Math
{
    // Naming: https://en.wikipedia.org/wiki/Template:Arithmetic_operations
    public static partial class MathExtensions
    {
        public static int MultiplyBy(this short multiplicand, short multiplier)
        {
            int product = multiplicand * multiplier;
            return product;
        }

        public static int MultiplyBy(this short multiplicand, int multiplier)
        {
            int product = multiplicand * multiplier;
            return product;
        }

        public static long MultiplyBy(this short multiplicand, long multiplier)
        {
            long product = multiplicand * multiplier;
            return product;
        }

        public static int MultiplyBy(this int multiplicand, short multiplier)
        {
            int product = multiplicand * multiplier;
            return product;
        }

        public static int MultiplyBy(this int multiplicand, int multiplier)
        {
            int product = multiplicand * multiplier;
            return product;
        }

        public static long MultiplyBy(this int multiplicand, long multiplier)
        {
            long product = multiplicand * multiplier;
            return product;
        }

        public static long MultiplyBy(this long multiplicand, short multiplier)
        {
            long product = multiplicand * multiplier;
            return product;
        }

        public static long MultiplyBy(this long multiplicand, int multiplier)
        {
            long product = multiplicand * multiplier;
            return product;
        }

        public static long MultiplyBy(this long multiplicand, long multiplier)
        {
            long product = multiplicand * multiplier;
            return product;
        }
    }
}