namespace Ardalis.Extensions.Math;

// Naming: https://en.wikipedia.org/wiki/Template:Arithmetic_operations
public static partial class MathExtensions
{
  public static int DivideBy(this short dividend, short divisor)
  {
    int quotient = dividend / divisor;
    return quotient;
  }

  public static int DivideBy(this short dividend, int divisor)
  {
    int quotient = dividend / divisor;
    return quotient;
  }

  public static long DivideBy(this short dividend, long divisor)
  {
    long quotient = dividend / divisor;
    return quotient;
  }

  public static int DivideBy(this int dividend, short divisor)
  {
    int quotient = dividend / divisor;
    return quotient;
  }

  public static int DivideBy(this int dividend, int divisor)
  {
    int quotient = dividend / divisor;
    return quotient;
  }

  public static long DivideBy(this int dividend, long divisor)
  {
    long quotient = dividend / divisor;
    return quotient;
  }

  public static long DivideBy(this long dividend, short divisor)
  {
    long quotient = dividend / divisor;
    return quotient;
  }

  public static long DivideBy(this long dividend, int divisor)
  {
    long quotient = dividend / divisor;
    return quotient;
  }

  public static long DivideBy(this long dividend, long divisor)
  {
    long quotient = dividend / divisor;
    return quotient;
  }
}
